public class PrintAlphabet {
    public static void main(String[] args) {
        System.out.println("=== Huruf A - Z ===");
        for (char huruf = 'A'; huruf <= 'Z'; huruf++) {
            System.out.println(huruf + " ");
        }

        System.out.println("\n");

        System.out.println("=== huruf a - z ===");
        for (char huruf = 'a'; huruf <= 'z'; huruf++) {
            System.out.println(huruf + " ");
        }
    }
}
